package com.brillio.org;

public class Loop {

	public static void main(String[] args) {
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		for(int j=20; j>=0; j--) 
		{
			System.out.println(j);
		}

		int[] a= {10,20,30,40,50,60};
		
		for( int k=0; k<=a.length; k++)
		{
			System.out.println(a[k]);
		}
	
	
	}

}
